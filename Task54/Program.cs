// Задача 54: Задайте двумерный массив. Напишите программу, которая упорядочит по убыванию элементы каждой строки двумерного массива.
// Например, задан массив:
// 1 4 7 2
// 5 9 2 3
// 8 4 2 4
// В итоге получается вот такой массив:
// 7 4 2 1
// 9 5 3 2
// 8 4 4 2

// Method--------------------------------------------------
int[,] CreateMatrixArrayInt(int row, int column, int min, int max)
{
	int[,] matrixArray = new int[row, column];
	Random rnd = new Random();
	for (int i = 0; i < matrixArray.GetLength(0); i++)
	{
		for (int j = 0; j < matrixArray.GetLength(1); j++)
		{
			matrixArray[i, j] = rnd.Next(min, max + 1);
		}
	}
	return matrixArray;
}
void PrintMatrix(int[,] matrix)
{
	for (int i = 0; i < matrix.GetLength(0); i++)
	{
		Console.Write("|");
		for (int j = 0; j < matrix.GetLength(1); j++)
		{
			if (j < matrix.GetLength(1) - 1) Console.Write($"{matrix[i, j],3}, ");
			else Console.Write($"{matrix[i, j],3}");
		}
		Console.WriteLine("|");
	}
}
void SortRowsMaxMInMatrix(int[,] matrix)
{

	for (int i = 0; i < matrix.GetLength(0); i++)
	{
		for (int j = 0; j < matrix.GetLength(1); j++)
		{
			for (int m = 0; m < matrix.GetLength(1); m++)
			{
				if (matrix[i, m] <= matrix[i, j])
				{
					int temp = matrix[i, m];
					matrix[i, m] = matrix[i, j];
					matrix[i, j] = temp;
				}
			}
		}
	}

}
// Program body--------------------------------------------
int[,] matrixArray = CreateMatrixArrayInt(3, 4, 1, 10);

Console.Clear();
PrintMatrix(matrixArray);
Console.WriteLine();
SortRowsMaxMInMatrix(matrixArray);
PrintMatrix(matrixArray);